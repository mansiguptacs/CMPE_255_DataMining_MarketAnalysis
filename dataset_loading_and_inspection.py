"""**Check Data Types and Missing Values**"""

def inspect_dataframe(df, name):
    print(f"\n{name} Info:")
    print("-" * 40)
    display(df.info())

    print("\nMissing Values:")
    display(df.isnull().sum())

    print("\nUnique Users:", df['user_id'].nunique() if 'user_id' in df.columns else "N/A")
    print("\n" + "="*60)

inspect_dataframe(orders, "Orders")
inspect_dataframe(order_products_prior, "Order Products Prior")
inspect_dataframe(products, "Products")

"""**Verify Key Relationships**"""

print("Verifying key relationships...")

# Check if user_id exists in orders
assert 'user_id' in orders.columns, "user_id not found in orders table"

# Check order_id linkage
